#include "../../cub3d.h"

int	quality_element(char *line, int i)
{
	if (line && line[i] > 32)
	{
		while (line[i] > 32)
			i++;
		while (line[i] <= 32)
			i++;
		while (line[i] > 32)
			i++;
		if (line[i] == '\0')
        	return (1);
    }
	return (0);
}
